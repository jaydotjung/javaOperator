public class StringDemo {
	public static void main(String[] args) {
		char c = 'c'; 
		// 맨 끝에 있는 ; 세미컬럼, 구분자라고 부른다. 
		// 카 데이터타입의 c 변수에 알파벳 c 값을 할당한다. 
		System.out.println(c);
		String s = "abc";
		System.out.println(s);
		System.out.println("abc");
		// 알파벳 하나만 출력할 경우는 char 타입을 사용하고, 하나 이상 복수의 
		//문자열을 출력할때는 Strng 타입을 상용한다.
		//주ㅢ할 점은 싱글커터와 더블 쿼터의 유의
	}

}
